# ── Helpers ───────────────────────────────────────────────────────────────────

def set_font(run, name="Times New Roman", size=12, bold=False,
             italic=False, color=None):
    run.font.name   = name
    run.font.size   = Pt(size)
    run.font.bold   = bold
    run.font.italic = italic
    if color:
        run.font.color.rgb = RGBColor(*color)

def add_heading(doc, text, level=1):
    p = doc.add_paragraph()
    p.paragraph_format.space_before    = Pt(14)
    p.paragraph_format.space_after     = Pt(4)
    p.paragraph_format.keep_with_next  = True
    run = p.add_run(text)
    if level == 0:
        set_font(run, size=18, bold=True)
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        p.paragraph_format.space_before = Pt(0)
        p.paragraph_format.space_after  = Pt(6)
    elif level == 1:
        set_font(run, size=13, bold=True)
        p.paragraph_format.space_before = Pt(18)
    elif level == 2:
        set_font(run, size=12, bold=True)
        p.paragraph_format.space_before = Pt(10)
    return p

def add_body(doc, text, indent=False, space_after=8):
    p = doc.add_paragraph()
    p.paragraph_format.space_after = Pt(space_after)
    if indent:
        p.paragraph_format.left_indent = Cm(0.8)
    p.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
    run = p.add_run(text)
    set_font(run)
    return p

def add_mixed(doc, parts, indent=False, space_after=8):
    p = doc.add_paragraph()
    p.paragraph_format.space_after = Pt(space_after)
    p.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
    if indent:
        p.paragraph_format.left_indent = Cm(0.8)
    for text, bold, italic in parts:
        run = p.add_run(text)
        set_font(run, bold=bold, italic=italic)
    return p

def add_separator(doc):
    p = doc.add_paragraph()
    p.paragraph_format.space_after  = Pt(2)
    p.paragraph_format.space_before = Pt(2)
    pPr = p._p.get_or_add_pPr()
    pBdr = OxmlElement('w:pBdr')
    bottom = OxmlElement('w:bottom')
    bottom.set(qn('w:val'), 'single')
    bottom.set(qn('w:sz'), '6')
    bottom.set(qn('w:space'), '1')
    bottom.set(qn('w:color'), 'AAAAAA')
    pBdr.append(bottom)
    pPr.append(pBdr)

def add_ref(doc, text):
    p = doc.add_paragraph()
    p.paragraph_format.left_indent        = Cm(1.0)
    p.paragraph_format.first_line_indent  = Cm(-1.0)
    p.paragraph_format.space_after        = Pt(4)
    run = p.add_run(text)
    set_font(run, size=11)
    return p

def add_image(doc, filename, caption, width=Inches(5.8)):
    """Insert an image centred with a caption below it, if the file exists."""
    path = os.path.join(SHOTS_DIR, filename)
    if not os.path.exists(path):
        # Insert a grey placeholder paragraph instead
        p = doc.add_paragraph()
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        p.paragraph_format.space_before = Pt(8)
        p.paragraph_format.space_after  = Pt(4)
        run = p.add_run(f"[Image not found: {filename}]")
        set_font(run, size=10, italic=True, color=(160, 160, 160))
    else:
        p = doc.add_paragraph()
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        p.paragraph_format.space_before = Pt(12)
        p.paragraph_format.space_after  = Pt(4)
        run = p.add_run()
        run.add_picture(path, width=width)

    # Caption
    cap = doc.add_paragraph()
    cap.alignment = WD_ALIGN_PARAGRAPH.CENTER
    cap.paragraph_format.space_after  = Pt(14)
    cap.paragraph_format.space_before = Pt(2)
    cap_run = cap.add_run(caption)
    set_font(cap_run, size=9, italic=True, color=(100, 100, 100))
    return cap

