"""
generate_report.py
Generates the Sentio critical academic report as a formatted .docx file.

Screenshots expected in docs/screenshots/:
  01_config_screen.png       — Configure Session screen
  02_device_settings.png     — Device Settings popup
  03_calibration_screen.png  — Calibration / Connecting screen
  04_signal_lost.png         — Signal Lost modal
  05_monitoring_screen.png   — Live Monitoring dashboard
  architecture.png           — Generated by generate_architecture_diagram.py

Run:
  python generate_architecture_diagram.py   # first time
  python generate_report.py
"""

